import mongoose from "mongoose";


const orderRequestSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phoneNumber: {
    type: String,
    required: true,
  },
  country: {
    type: String,
    required: true,
  },
  city: {
    type: String,
    required: true,
  },
  postalCode: {
    type: Number,
    required: true,
  },
  status: {
    type: String,
    default: 'pending'
  },
});

const OrderRequest = mongoose.model('OrderRequest', orderRequestSchema);

export default OrderRequest
